package Operations;

public class Challenge4 {
	public static void main(String[] args) {
		 int a =  12 ;
	        int b = 10 ;

	        // Bitwise operations
	        int andResult = a & b;
	        int orResult = a | b;
	        int xorResult = a ^ b;
	        
	        
	        System.out.println(a + " & " + b + " = " + andResult);
	        System.out.println(a + " | " + b + " = " + orResult);
	        System.out.println(a + " ^ " + b + " = " + xorResult);

	}

}
